void clear_screen(int width, int height)
{
    int x = 0;
    int y = 0;

    for(x=0; x<width; x++)
    {
        for(y=0; y<height; y++)
        {
            long int position = (x + vinfo.xoffset) * (vinfo.bits_per_pixel / 8) +
               (y + vinfo.yoffset) * finfo.line_length;
            *(fbp + position) = 0;
            *(fbp + position + 1) = 0;
            *(fbp + position + 2) = 0;
            *(fbp + position + 3) = 0;
        }
    }
}


void refresh(int x0, int x1, int y0, int y1)
{
    int x = 0;
    int y = 0;

    for(x=x0; x<x1; x++)
    {
        for(y=y0; y<y1; y++)
        {
        	if((y<0) || (y>layary)){

			}
			else{
	        	buffer_r[x][y] = 0;
	        	buffer_g[x][y] = 0;
	        	buffer_b[x][y] = 0;
	        }
        }
    }
}
